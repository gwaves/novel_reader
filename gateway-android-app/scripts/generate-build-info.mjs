#!/usr/bin/env node

import { execFileSync } from 'node:child_process'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const appRoot = dirname(fileURLToPath(new URL('../package.json', import.meta.url)))
const repoRoot = dirname(appRoot)
const packageJsonPath = join(appRoot, 'package.json')
const buildInfoJsonPath = join(appRoot, 'build-info.json')
const buildInfoTsPath = join(appRoot, 'src/generated/buildInfo.ts')

main().catch((error) => {
  console.error(`生成 Android 版本信息失败：${error.message}`)
  process.exitCode = 1
})

async function main() {
  const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'))
  const baseVersion = readNonEmptyString(packageJson.version) || '0.0.0'
  const gitCommit = readGit(['rev-parse', '--short=12', 'HEAD']) || 'unknown'
  const gitCommitFull = readGit(['rev-parse', 'HEAD']) || 'unknown'
  const gitCommitCount = readPositiveInteger(readGit(['rev-list', '--count', 'HEAD'])) ?? 0
  const buildNumber = readBuildNumber(process.env.GATEWAY_ANDROID_BUILD_NUMBER) ?? gitCommitCount
  const dirty = Boolean(readGit(['status', '--porcelain']))
  const buildTime = new Date().toISOString()
  const versionName = `${baseVersion}+build.${buildNumber}.g${gitCommit}${dirty ? '.dirty' : ''}`
  const versionCode = buildVersionCode(baseVersion, buildNumber)
  const info = {
    schemaVersion: 1,
    appName: 'AI小说助手',
    baseVersion,
    versionName,
    versionCode,
    buildNumber,
    gitCommit,
    gitCommitFull,
    dirty,
    buildTime,
  }

  await mkdir(dirname(buildInfoTsPath), { recursive: true })
  await writeFile(buildInfoJsonPath, `${JSON.stringify(info, null, 2)}\n`)
  await writeFile(
    buildInfoTsPath,
    `// Generated by scripts/generate-build-info.mjs. Do not edit.\nexport const buildInfo = ${JSON.stringify(info, null, 2)} as const\n`,
  )
}

function buildVersionCode(version, buildNumber) {
  const [major, minor, patch] = version.split(/[+-]/)[0].split('.').map((part) => readPositiveInteger(part) ?? 0)
  return major * 100_000_000 + minor * 1_000_000 + patch * 10_000 + Math.min(buildNumber, 9_999)
}

function readGit(args) {
  try {
    return execFileSync('git', args, { cwd: repoRoot, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim()
  } catch {
    return ''
  }
}

function readNonEmptyString(value) {
  return typeof value === 'string' && value.trim() ? value.trim() : ''
}

function readPositiveInteger(value) {
  const parsed = Number(String(value ?? '').trim())
  if (!Number.isInteger(parsed) || parsed < 0) return undefined
  return parsed
}

function readBuildNumber(value) {
  const parsed = readPositiveInteger(value)
  return parsed && parsed > 0 ? parsed : undefined
}
